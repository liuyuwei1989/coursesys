package com.bwf.courses.utils;

public class StudentCourseInfo {
	private String name;
	private String className;
	private String loginName;
	private String tueCourseName;
	private String friCourseName;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getClassName() {
		return className;
	}
	public void setClassName(String className) {
		this.className = className;
	}
	public String getLoginName() {
		return loginName;
	}
	public void setLoginName(String loginName) {
		this.loginName = loginName;
	}
	public String getTueCourseName() {
		return tueCourseName;
	}
	public void setTueCourseName(String tueCourseName) {
		this.tueCourseName = tueCourseName;
	}
	public String getFriCourseName() {
		return friCourseName;
	}
	public void setFriCourseName(String friCourseName) {
		this.friCourseName = friCourseName;
	}
	
}
